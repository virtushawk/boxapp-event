package com.virtushawk.boxappevent.model.dto.controller;

/**
 * DTO that contains event information
 */
public class EventDTO {

    private String id;

    private String name;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
